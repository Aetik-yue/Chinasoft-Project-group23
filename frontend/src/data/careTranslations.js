const SUPPORTED_LANGUAGES = new Set(['en', 'es', 'ja'])

/**
 * Translate one care-profile display value.
 * Chinese, unsupported languages, non-string values, and unknown strings are
 * returned unchanged so profile/API data remains lossless.
 */
export function translateCareText(value, language) {
  if (typeof value !== 'string') return value

  const normalizedLanguage = String(language || '').trim().toLowerCase().split(/[-_]/)[0]
  if (!SUPPORTED_LANGUAGES.has(normalizedLanguage)) return value

  return CARE_TEXT_TRANSLATIONS[value]?.[normalizedLanguage] || value
}
